text = ""
for i in range(len(sys.argv)-1):
    text += sys.argv[i+1] + " "
    text = text[:-1].upper()

try:
    morse_code = ""
    for i in text:
        morse_code += MORSE_CODE_DICT[i] + ' '
    print(morse_code)
except:
    print("ERROR")
